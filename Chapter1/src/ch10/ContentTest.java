public class ContentTest {

	public static void main(String[] args) {

		final int MAX_NUM = 100;
		final int MIN_NUM;
		
		MIN_NUM = 0;
		
		System.out.println(MAX_NUM);
		System.out.println(MIN_NUM);
		
		// final  --> 상수 
		// 상수는 변하지 않는 값으로 다른값 대입이 안됨 --> 보통 대문자로 선언
		// 상수는 초기값을 선언해주어야 한다. 
		// 상수는 변하지 않는 값을 반복하여 사용할 때 의미 있는 문자로 가독성을 높이는데 사용
		// 변하더라도 선언한 부분만 변경하면 되는 장점

	}

}
